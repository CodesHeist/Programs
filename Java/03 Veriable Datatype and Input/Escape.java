
public class Escape{
    public static void main(String[] args) {
        // Here we are using \" to write "" within String...
        System.out.println("This is \"PRIYANSHU\" ");
        // \n helps in printing the output in new line...
        System.out.println("hello \n Priyanshu");
        // \b helps in removing the privious char from the output...
        System.out.println("hi\bpriyanshu");
        // \t helps in using the tab in the output...
        System.out.println("hello\tpriyanshu");
        // \\ helps us to use the backslash in the output...
        System.out.println("\\priyanshu\\");
        // \' helps us to use the single qoute in the output
        System.out.println("Hello \'World\'");
    }
}